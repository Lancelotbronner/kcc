#pragma once

#include <stdint.h>

enum parser_context : uint32_t {
	CTX_DECLARATION = 0x1,
	CTX_DEFINITION = 0x2,
	CTX_MEMBER = 0x4,

	CTX_STRUCT_DECLARATION = 0x8,
	CTX_STRUCT_DEFINITION = 0x10,
	CTX_STRUCT_MEMBER = 0x20,

	CTX_UNION_DECLARATION = 0x40,
	CTX_UNION_DEFINITION = 0x80,
	CTX_UNION_MEMBER = 0x100,

	CTX_ENUM_DECLARATION = 0x2'00,
	CTX_ENUM_DEFINITION = 0x4'00,
	CTX_ENUM_MEMBER = 0x8'00,

	CTX_FUNCTION_DECLARATION = 0x10'00,
	CTX_FUNCTION_PARAMETER = 0x20'00,
	CTX_FUNCTION_DEFINITION = 0x40'00,

	CTX_TYPEDEF = 0x80'00,

	CTX_LOCAL_VARIABLE = 0x1'00'00,
	CTX_GLOBAL_VARIABLE = 0x2'00'00,

	CTX_LABEL = 0x4'00'00,
};
